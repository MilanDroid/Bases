  CREATE OR REPLACE PROCEDURE "FLIPBOARD"."ACTUALIZAR_USUARIO" (P_CODIGO_USUARIO in INTEGER, P_NOMBRE IN VARCHAR2,P_ALIAS IN VARCHAR2, P_DESCRIPCION IN  VARCHAR2 )
IS
  NOMBRE_OLD VARCHAR2(500);
  ALIAS_OLD VARCHAR2(500);
  DESCR_OLD VARCHAR2(500);
BEGIN
    SELECT NOMBRE_USUARIO INTO NOMBRE_OLD FROM TBL_USUARIOS WHERE CODIGO_USUARIO=P_CODIGO_USUARIO;
    SELECT ALIAS_USUARIO INTO ALIAS_OLD FROM TBL_USUARIOS WHERE CODIGO_USUARIO=P_CODIGO_USUARIO;
    SELECT DESCRIPCION_USUARIO INTO DESCR_OLD FROM TBL_USUARIOS WHERE CODIGO_USUARIO=P_CODIGO_USUARIO;
    IF TRIM(UPPER(NOMBRE_OLD))!=TRIM(UPPER(P_NOMBRE)) THEN
        UPDATE TBL_USUARIOS SET NOMBRE_USUARIO=INITCAP(P_NOMBRE) WHERE CODIGO_USUARIO = P_CODIGO_USUARIO;
    END IF;
    IF TRIM(UPPER(ALIAS_OLD))!=TRIM(UPPER(P_ALIAS)) THEN
        UPDATE TBL_USUARIOS SET ALIAS_USUARIO=P_ALIAS WHERE CODIGO_USUARIO = P_CODIGO_USUARIO;
    END IF;
    IF TRIM(UPPER(DESCR_OLD))!=TRIM(UPPER(P_DESCRIPCION)) OR DESCR_OLD IS NULL THEN
        UPDATE TBL_USUARIOS SET DESCRIPCION_USUARIO=P_DESCRIPCION WHERE CODIGO_USUARIO = P_CODIGO_USUARIO;
    END IF;
END;

